package curs5;

public class WhileExemple {

	public static void main(String[] args) {

		for (int i = 0; i<10; i++) {
			
			System.out.println("Valoare lui i este :" + i);	
					
				}
		System.out.println("------------");
		
		int j = 0;
		
		while(j<10) {
			
			System.out.println("Valoare lui i este :" + j);
			j++;
		}
		
		
		
		
	}

}
